public class User {

    private String name;
    private String phone;

    public void setName(String name) {
        this.name = name;
    }

    public String getPhone() {
        return phone;
    }

    public void setPhone(String phone) {
        this.phone = phone;
    }

    public String getName(){
        return this.name;
    }

}
